#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int i, char *))
{
	char			*str;
	unsigned int	i;

	str = s;
	i = 0;
	while (*str)
	{
		(*f)(i, str);
		str++;
		i++;
	}
}
